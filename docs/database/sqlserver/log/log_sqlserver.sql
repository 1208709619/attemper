CREATE TABLE LOG_OP (
  ID NVARCHAR(64) NOT NULL,
  TENANT_ID NVARCHAR(64) ,
  CLASS_NAME NVARCHAR(255) ,
  METHOD NVARCHAR(255) ,
  TAG NVARCHAR(255) ,
  OPERATION NVARCHAR(255) ,
  PATH NVARCHAR(255) ,
  PARAM TEXT,
  RESULT TEXT,
  CODE INT,
  MSG TEXT,
  HANDLE_TIME BIGINT,
  DURATION NVARCHAR(30) ,
  IP NVARCHAR(255) ,
  PRIMARY KEY (ID)
);
