
CREATE TABLE log_op (
  ID VARCHAR(64) NOT NULL,
  TENANT_ID VARCHAR(64) ,
  CLASS_NAME VARCHAR(255) ,
  METHOD VARCHAR(255) ,
  TAG VARCHAR(255) ,
  OPERATION VARCHAR(255) ,
  PATH VARCHAR(255) ,
  PARAM TEXT,
  RESULT TEXT,
  CODE INTEGER,
  MSG TEXT,
  HANDLE_TIME BIGINT,
  DURATION VARCHAR(30),
  IP VARCHAR(255) ,
  PRIMARY KEY (ID)
);