
CREATE TABLE log_op (
  ID NVARCHAR2(64) NOT NULL,
  TENANT_ID NVARCHAR2(64) ,
  CLASS_NAME NVARCHAR2(255) ,
  METHOD NVARCHAR2(255) ,
  TAG NVARCHAR2(255) ,
  OPERATION NVARCHAR2(255) ,
  PATH NVARCHAR2(255) ,
  PARAM CLOB,
  RESULT CLOB,
  CODE NUMBER(11) ,
  MSG CLOB,
  HANDLE_TIME NUMBER(13),
  DURATION NVARCHAR2(30) ,
  IP NVARCHAR2(255) ,
  PRIMARY KEY (ID)
);

